// program to demonstrate the precision of float and double
package basic_package;

public class flot_vs_double {

    public static void main(String[] args) {

        System.out.print("\nDOUBLE PRECISION 1.0D / 3.0D = " + 1.0D / 3.0D); // print the result upto 15 to 17 decimal number
        System.out.print("\nFLOAT PRECISION  1.0F / 3.0F = " + 1.0F / 3.0F); // prints the result upto 7 to 8 decimal number
    }
}
